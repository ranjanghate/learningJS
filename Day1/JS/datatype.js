// Primitive Datatype in JS

  // String 
let name = 'Ranjan';
name = "Ranjan";
name = `Ranjan`;
console.log(name);
  // Concate string
console.log("Hey "+name)
console.log(typeof (name));

  // Number
let x = 25;
console.log(x*2 + x/5 + x-2); 

  // Bool
let check = false;
console.log(check);
console.log(typeof (check));

  // Null
let n = null;
console.log(n);
console.log(typeof (n));

  // Undefine
let u = undefined;
console.log(u);
console.log(typeof (u));

// Reference DataType

  // Array
  const arr = [1, 2, 3, 4, false]
  console.log(arr)
  console.log(typeof arr)

